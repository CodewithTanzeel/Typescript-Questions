// Declare and initialize a variable 'count' with a value of 10.
let count: number = 10;

// Start a 'while' loop that runs as long as 'count' is greater than or equal to 0.
while (count >= 0) {

    // If 'count' equals 5, exit the loop using the 'break' statement.
    if (count === 5) {
        break; // Break the loop when 'count' equals 5.
    }

    // Log the current value of 'count' to the console.
    console.log(count); // Prints the current count value.

    // Decrement 'count' by 1 in each iteration.
    count--;
}
