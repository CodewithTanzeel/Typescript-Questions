let i = 1;
// Initializing the variable 'i' with a value of 1 outside the loop

while (i <= 5) { 
  // Condition: loop continues as long as 'i' is less than or equal to 5

  console.log(i, "Hello World"); 
  // Logs the current value of 'i' followed by "Hello World"

  i++; 
  // Increment: Increases the value of 'i' by 1 after each iteration
}
